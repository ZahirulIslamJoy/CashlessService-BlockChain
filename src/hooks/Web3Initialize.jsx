export const contactId = "0x204Da9Aa083E6070629c66CE1591c760dbC26422";



 export async function initializeWeb3() {
	if (!window.ethereum) {
		Swal.fire({
		  position: "top-end",
		  icon: "error",
		  title: "Please Install MetaMask First",
		  showConfirmButton: false,
		  timer: 200,
		}).then(() => {
		  window.location.href = "/";
		});
	  }
	  

    if (window.ethereum) {
      const web3Instance = new Web3(window.ethereum);
    //   setWeb3(web3Instance);
      try {
        const accounts = await window.ethereum.request({
          method: "eth_requestAccounts",
        });
        const  account =accounts[0];

        const contractInstance = new web3Instance.eth.Contract(
          CashLessContract,
          contactId // Use the provided contract address here
        );
        // setContract(contractInstance);
        return {web3Instance,account,contractInstance}
      } 
      catch (error) {
         return ("Error initializing Web3:", error);
      }
    } else {
      return ("MetaMask not detected.");
    }
  }

 

